CREATE SCHEMA asm1;
USE asm1;

-- CREATE TABLES

-- CREATE TABLE TEAM
CREATE TABLE TEAM
	(TeamID INT PRIMARY KEY,
     Team_Name VARCHAR(50)
     );
     
-- CREATE TABLE ROOM
CREATE TABLE ROOM
	(Room_Number INT PRIMARY KEY,
	 Number_Of_People INT NOT NULL, 
     Need TEXT
     );

-- CREATE TABLE ATHLETE
CREATE TABLE ATHLETE
	(AthleteID INT PRIMARY KEY,
     Athlete_Name VARCHAR(50) NOT NULL,
     Nationality VARCHAR(50) NOT NULL,
     Age INT NOT NULL,
     TeamID INT,
     Room_Number INT,
     FOREIGN KEY (TeamID) REFERENCES TEAM(TeamID),
     FOREIGN KEY (Room_Number) REFERENCES ROOM(Room_Number)
     );
   
-- CREATE TABLE VENUE
CREATE TABLE VENUE
	(VenueID INT PRIMARY KEY,
     Venue_Name VARCHAR(50) NOT NULL,
     Venue_Location TEXT
     );
   
-- CREATE TABLE SPORT
CREATE TABLE SPORT
	(Sport_Code INT PRIMARY KEY,
     Sport_Name VARCHAR(50) NOT NULL
     );
     
-- CREATE TABLE EQUIPMENT
CREATE TABLE EQUIPMENT
	(Equipment_Number INT PRIMARY KEY,
     Equipment_Name VARCHAR(50) NOT NULL,
     Inventory_Level INT,
     Distribution INT,
     Maintenance_schedule TEXT, 
     Sport_Code INT, 
     FOREIGN KEY (Sport_Code) REFERENCES SPORT(Sport_Code),
     FOREIGN KEY (Distribution) REFERENCES VENUE(VenueID)
     );
   
-- CREATE TABLE EVENT
CREATE TABLE EVENT
	(Event_Code INT PRIMARY KEY,
     Event_Name VARCHAR(50) NOT NULL,
     Date_Time DATETIME,
     Sport_Code INT,
     VenueID INT,
     FOREIGN KEY (Sport_Code) REFERENCES SPORT(Sport_Code),
     FOREIGN KEY (VenueID) REFERENCES VENUE(VenueID)
     );
     
-- CREATE TABLE PARTICIPATION
CREATE TABLE PARTICIPATION
	(AthleteID INT,
     Event_Code INT,
     PRIMARY KEY (AthleteID, Event_Code),
     FOREIGN KEY (AthleteID) REFERENCES ATHLETE(AthleteID),
     FOREIGN KEY (Event_Code) REFERENCES EVENT(Event_Code)
     );
     
-- CREATE TABLE VOLUNTEER
CREATE TABLE VOLUNTEER
	(VolunteerID INT PRIMARY KEY,
     Volunteer_Name VARCHAR(50) NOT NULL,
     Role VARCHAR(50)
     );

-- CREATE TABLE TRAINING
CREATE TABLE TRAINING
	(Event_Code INT,
	 VolunteerID INT,
     Training_DateTime DATETIME,
     PRIMARY KEY (Event_Code, VolunteerID),
     FOREIGN KEY (Event_Code) REFERENCES EVENT(Event_Code),
     FOREIGN KEY (VolunteerID) REFERENCES VOLUNTEER(VolunteerID)
     );
   
